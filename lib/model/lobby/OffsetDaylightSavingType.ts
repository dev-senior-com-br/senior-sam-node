enum OffsetDaylightSavingType {
     /**
     * Sem configuração de horário de verão
     */
    NONE_OFFSET,
    /**
     * Uma hora a menos no horário de verão
     */
    MINUS_ONE_HOUR,
    /**
     * Uma hora a mais no horário de verão
     */
    PLUS_ONE_HOUR
}
export = OffsetDaylightSavingType;